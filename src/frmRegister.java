    public void next()
    {
        final JOptionPane jo = new JOptionPane();
        int i = txtPno.getText().length();
        String emailID = txtEmail.getText();
        String emailRE = "^[_A-Za-z0-9-]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$" ;

        Boolean flag = emailID.matches(emailRE);
       
        if(txtPno.getText().equals("") || (!rdbFemale.isSelected() && !rdbMale.isSelected()) || txtEmail.getText().equals("") || txtAddress.getText().equals(""))
        {
            JOptionPane.showMessageDialog(jo,"Please fill all the fields!!!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(i!=10)
        {
            JOptionPane.showMessageDialog(jo,"Please enter a valid mobile no!!!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!flag)
        {
            JOptionPane.showMessageDialog(jo,"Please enter a valid email ID!!!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String gender;

        if(frmRegister.rdbMale.isSelected())
            gender = frmRegister.rdbMale.getText();
        else
        {
        if(frmRegister.rdbFemale.isSelected())
            gender = frmRegister.rdbFemale.getText();
        else
            gender = null;
        }


        Vector v = new Vector();
        v.add(lblid.getText());
        v.add(txtName.getText());
        v.add(txtPno.getText());
        v.add(gender);
        v.add(txtEmail.getText());
        v.add(txtAddress.getText());
        v.add(lblDate.getText());
        new frmDisplay(v).setVisible(true);
        this.setVisible(false);
    }
    
