    private void txtNameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyPressed
        
        final KeyEvent k=evt;
        
        txtName.addKeyListener(new KeyAdapter()
        {
            public void keyTyped(KeyEvent e)
            {
              final char c=k.getKeyChar();
              if(Character.isDigit(c))
              {
                  txtName.setBackground(Color.red);
                  txtName.setEditable(false);
              }
              else
              {
                  txtName.setBackground(Color.LIGHT_GRAY);;
                  txtName.setEditable(true);
              }
            }
        });
    }//GEN-LAST:event_txtNameKeyPressed

    private void txtPnoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPnoKeyPressed
        
        final KeyEvent k=evt;

        txtPno.addKeyListener(new KeyAdapter()
        {

            public void keyTyped(KeyEvent e) 
            {
                  final char c=k.getKeyChar();
                  if (((c < '0') || (c > '9')) && (c != '\b')) 
                  {
                    txtPno.setBackground(Color.red);
                    txtPno.setEditable(false);
                  }
                  else
                  {
                      txtPno.setEditable(true);
                      txtPno.setBackground(Color.lightGray);
                  }
        }
        });
    }//GEN-LAST:event_txtPnoKeyPressed

