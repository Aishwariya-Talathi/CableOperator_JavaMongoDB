    public void update()
    {
        int x = tblChannels.getSelectedRow();
        System.out.println(x);
        
        if(txtPackName.getText().equals("") || txtPackCost.getText().equals("") || (x==-1 && lblChnl.getText().equals("")))
        {
            final JOptionPane jo = new JOptionPane();
            JOptionPane.showMessageDialog(jo,"Please fill all the fields!!!","Error",JOptionPane.ERROR_MESSAGE);
        }
         
        Connection c = new Connection();
        DBCollection dbc = c.connection("Package");
        BasicDBObject bdb = new BasicDBObject();
        BasicDBObject search = new BasicDBObject();
        search.put("Pid",lblid.getText());
        
        String[] colNames = {"Pid","Package Name","Channels","Package Cost"};
        DefaultTableModel dtm = new DefaultTableModel(colNames,0);
        
        DefaultTableModel dtm1 = (DefaultTableModel) tblChannels.getModel();

        int s = tblChannels.getSelectedRowCount();
                
        int[] ab = tblChannels.getSelectedRows();
               
        BasicDBList bdl = new BasicDBList();
        for(int i = 0; i<s;i++)
        {
            bdl.add(tblChannels.getValueAt(ab[i],0).toString());
        }
        
        bdb.put("Pid", lblid.getText());
        bdb.put("Pack_name",txtPackName.getText());
        
        if(x==-1)
        {
            bdb.put("Channels",lblChnl.getText());
        }
        else
            bdb.put("Channels", bdl);
     
        bdb.put("Pack_cost",txtPackCost.getText());

        
        dbc.update(search,bdb);
        tblPackage.setModel(dtm);
        tblPackage.repaint();
        tblChannels.repaint();
        dtm1.fireTableDataChanged();
        
        lblid.setText("");
        lblChnl.setText("");
        txtPackCost.setText("");
        txtPackName.setText("");
        
    }
    
    
