    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
       
        Connection j = new Connection();
        DBCollection dbc = j.connection("Package");
        String[] colNames = {"Pack ID","Package Name","Channels","Pack Cost"};
        DefaultTableModel dtm = new DefaultTableModel(colNames,0);
        
        DBCursor cursor = dbc.find();
        
        String c1  = "";
               
        while(cursor.hasNext())
        {
            DBObject obj = cursor.next();
            String pid = (String)obj.get("Pid");
            String pname = (String)obj.get("Pack_name");
            String cost = (String)obj.get("Pack_cost");
            
            BasicDBObject bdb = new BasicDBObject();
            bdb.put("Pid",pid);
            DBCursor cursor1 = dbc.find(bdb);
            int i = 0;
            while(cursor1.hasNext())
            {
                c1 = cursor1.next().get("Channels").toString();
            }
            dtm.addRow(new Object[]{pid,pname,c1,cost});
            tblPackage.setModel(dtm);
        }
        
        DBCollection dbc1 = j.connection("Channels");
        
        String[] colNames1 = {"Channels"};
        DefaultTableModel dtm1 = new DefaultTableModel(colNames1,0);
        DBCursor cursor1 = dbc1.find();
        
        while(cursor1.hasNext())
        {
            DBObject obj1 = cursor1.next();
            String name = (String)obj1.get("Cname");
            dtm1.addRow(new Object[]{name});
            tblChannels.setModel(dtm1);
        }
        
        lblChnl.setText(null);
        txtPackName.setText(null);
        txtPackCost.setText(null);
        lblid.setText(null);
    }//GEN-LAST:event_formWindowActivated

    public void delete()
    {
        Connection c = new Connection();
            DBCollection dbc = c.connection("Package");

            int i = tblPackage.getSelectedRow();
            
            if(i==-1)
            {
                final JOptionPane jo = new JOptionPane();
                JOptionPane.showMessageDialog(jo,"No Row selected!!!","Error",JOptionPane.ERROR_MESSAGE);
            }

            
            JDialog.setDefaultLookAndFeelDecorated(true);
            int response = JOptionPane.showConfirmDialog(null, "Do you want to continue?", "Confirm",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response == JOptionPane.YES_OPTION)
            {   
                String id = tblPackage.getValueAt(i,0).toString();

                BasicDBObject document = new BasicDBObject();
                document.put("Pid",id);
                dbc.remove(document);

                final JOptionPane jo = new JOptionPane();
                JOptionPane.showMessageDialog(jo,"Package Deleted!!!","Success",JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(true);
            } 
            else if (response == JOptionPane.NO_OPTION) 
            {   
            }        
    }
    
