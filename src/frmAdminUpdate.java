    public void display()
    {
        try{
        
        Connection j = new Connection();
        DBCollection dbc = j.connection("Package");
        
        String[] colNames = {"Pid","Package Name ","Channels","Pack Cost"};
        DefaultTableModel dtm = new DefaultTableModel(colNames,0);
        DBCursor cursor = dbc.find();
       
        String c1 = "";
        
        while(cursor.hasNext())
        {
            DBObject obj = cursor.next();
            String pid = (String)obj.get("Pid");
            String pname = (String)obj.get("Pack_name");
            BasicDBObject bdb = new BasicDBObject();
            bdb.put("Pid",pid);
            DBCursor cursor1 = dbc.find(bdb);
            while(cursor1.hasNext())
            {
                c1 = cursor1.next().get("Channels").toString();
            }
            String cost = (String)obj.get("Pack_cost");
            
            dtm.addRow(new Object[]{pid,pname,c1,cost});
            tblPackage.setModel(dtm);
        }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }
    
